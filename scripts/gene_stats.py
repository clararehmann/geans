def calc_pi(sequence_array, nsites):
    """Calculate nucleotide diversity (pi) for a given sequence array."""
    # manually calculate pi, stolen from scikit-allel
    if sequence_array is None or len(sequence_array) == 0:
        return 0
    else:
        ac = sequence_array.count_alleles()
        an = np.sum(ac, axis=1)
        n_pairs = an * (an - 1) / 2
        n_same = np.sum(ac * (ac - 1) / 2, axis=1) 
        n_diff = n_pairs - n_same
        mpd = n_diff / n_pairs
        mpd_sum = np.sum(mpd)
        pi = mpd_sum / nsites if nsites > 0 else 0
        return pi

def calc_theta(sequence_array, nsites):
    """Calculate Watterson's theta (theta hat per base) for a given sequence array."""
    # manually calculate Watterson's estimator (theta hat per base)
    # stolen from scikit-allel
    if sequence_array is None or len(sequence_array) == 0:
        return 0
    else:
        ac = sequence_array.count_alleles()
        # count segregating variants
        S = ac.count_segregating()
        # assume number of chromosomes sampled is constant for all variants
        n = ac.sum(axis=1).max()
        # (n-1)th harmonic number
        a1 = np.sum(1 / np.arange(1, n))
        # calculate absolute value
        theta_hat_w_abs = S / a1
        # calculate value per base
        theta_hat_w = theta_hat_w_abs / nsites if nsites > 0 else 0
        return theta_hat_w

def calc_taj(sequence_array, nsites, min_sites=3):
    """Manually calculate Tajima's D."""
    # stolen from scikit-allel
    if sequence_array is None or len(sequence_array) == 0:
        return 0
    ac = sequence_array.count_alleles()
    S = ac.count_segregating()
    if S < min_sites:
        return np.nan
    # assume number of chromosomes sampled is constant for all variants
    n = ac.sum(axis=1).max()
    # (n-1)th harmonic number
    a1 = np.sum(1 / np.arange(1, n))
    # calculate Watterson's theta (absolute value)
    theta_hat_w_abs = S / a1
    # calculate mean pairwise difference
    an = np.sum(ac, axis=1)
    n_pairs = an * (an - 1) / 2
    n_same = np.sum(ac * (ac - 1) / 2, axis=1) 
    n_diff = n_pairs - n_same
    mpd = n_diff / n_pairs
    # calculate theta_hat pi (sum differences over variants)
    theta_hat_pi_abs = np.sum(mpd)
    # N.B., both theta estimates are usually divided by the number of
    # (accessible) bases but here we want the absolute difference
    d = theta_hat_pi_abs - theta_hat_w_abs
    # calculate the denominator (standard deviation)
    a2 = np.sum(1 / (np.arange(1, n)**2))
    b1 = (n + 1) / (3 * (n - 1))
    b2 = 2 * (n**2 + n + 3) / (9 * n * (n - 1))
    c1 = b1 - (1 / a1)
    c2 = b2 - ((n + 2) / (a1 * n)) + (a2 / (a1**2))
    e1 = c1 / a1
    e2 = c2 / (a1**2 + a2)
    d_stdev = np.sqrt((e1 * S) + (e2 * S * (S - 1)))
    # finally calculate Tajima's D
    D = d / d_stdev if d_stdev > 0 else np.nan

    return D

def calc_fst_btwn(gene, seqs, loc1, loc2):
    """Calculate Hudson's Fst between two locations."""
    locs = gene.locs
    assert sum(seqs.count_hom_ref(axis=1) == seqs.shape[1]) == 0 # no all-ref variants
    ind1 = np.where((locs == loc1).all(1))[0]
    ind2 = np.where((locs == loc2).all(1))[0]
    ac1 = seqs[:, ind1].count_alleles()
    ac2 = seqs[:, ind2].count_alleles()
    n, d = allel.hudson_fst(ac1, ac2, fill=np.nan)
    with np.errstate(invalid='ignore'):
        fst = np.nansum(n) / np.nansum(d)
    return fst

def calc_fst_pairwise(gene, seqs):
    """Calculate pairwise Fst between all locations."""
    locs = np.unique(gene.locs, axis=0)
    fstmat = pd.DataFrame(index=[tuple(i) for i in locs], columns=[tuple(i) for i in locs])
    for i, j in itertools.combinations(locs, 2):
        itup = tuple(i)
        jtup = tuple(j)
        fst = calc_fst_btwn(gene, seqs, i, j)
        fstmat[itup][jtup] = fst
        fstmat[jtup][itup] = fst
    return fstmat 

