class Gene:
    def __init__(self, name, chromosome=None, start=None, end=None, statistics=['pi','tajD','theta'], samples=None,
                 vcf=None, annotation=None, fasta=None):
        self._name = name
        self._chromosome = chromosome
        self._start = start
        self._end = end
        self._length = self._end - self._start + 1 if self._start is not None and self._end is not None else None
        self._gc_content = None # GC content will be calculated later
        self._samples = samples
        self._locs = None
        self.transcripts = None
        self.statistics = statistics
        self.vcf = None
        self.annotation = None
        self.fasta = None

    @property
    def samples(self, samples=None):
        if samples is not None:
            self._samples = samples
        if type(self._samples) is pd.DataFrame:
            return self._samples
        elif type(self._samples) is list or type(self._samples) is np.ndarray:
            return pd.DataFrame(self._samples, columns=['sample_id'])
        elif os.path.isfile(self._samples):
            self._samples = pd.read_csv(self._samples, sep='\t')
            return self._samples
    
    @property
    def name(self):
        return self._name

    def fetch_gene_coordinates(self, annotation, fasta):
        """Fetch gene coordinates from a GFF database generated by parse_gff.py."""
        self.annotation = annotation
        self.fasta = fasta
        db = gffutils.FeatureDB(annotation, keep_order=True)
        feature = db[self._name]
        self.chromosome = feature.chrom
        self.start = feature.start
        self.end = feature.end
        self.length = self.end - self.start + 1
        # calculate GC content
        seq = feature.sequence(fasta, use_strand=False)
        self.gc_content = (seq.count('G') + seq.count('C')) / len(seq) if len(seq) > 0 else 0
        return feature.chrom, feature.start, feature.end
    
    def fetch_gene_transcripts(self, transcriptID='mRNA'):
        """Fetch transcripts for a given gene from a GFF database."""
        db = gffutils.FeatureDB(self.annotation, keep_order=True)
        transcripts = list(db.children(self.name, featuretype=transcriptID))
        self.transcripts = tuple([Transcript(id=tx.id, gene=self.name, annotation=self.annotation, fasta=self.fasta,
                                        chromosome=tx.chrom, start=tx.start, end=tx.end, statistics=self.statistics, vcf=self.vcf)
                                  for tx in transcripts])
        return transcripts

    def load_variation(self, vcf, samples=None, filter=None, protein_fasta=None, locs=None):
        """Fetch variants for all transcripts of a given gene from a VCF file."""
        self.vcf = vcf
        for tx in self.transcripts:
            tx.load_variation(vcf, samples=samples, filter=filter, protein_fasta=protein_fasta, locs=locs)
        self._samples = self.transcripts[0].samples
        self._locs = self.transcripts[0].locs
        return self._samples, self._locs

    def calculate_statistics(self):
        """Calculate statistics for all transcripts of a given gene."""
        stats = {}
        for tx in self.transcripts:
            tx_stats = tx.calculate_statistics()
            stats[tx.id] = tx_stats
        self.statistics = stats
        return stats

class Transcript:
    def __init__(self, id, gene, annotation, fasta, vcf, chromosome=None, start=None, end=None, statistics=None):
        db = gffutils.FeatureDB(annotation, keep_order=True)
        cds = list(db.children(id, featuretype='CDS'))
        self._cds_id = [c.id for c in cds]
        self._cds_start = [c.start for c in cds]
        self._cds_end = [c.end for c in cds]
        self._cds_seq = [c.sequence(fasta, use_strand=False) for c in cds]
        self._cds_strand = cds[0].strand if cds else None
        self.id = id
        self._gene = gene
        self._chromosome = chromosome
        self._start = start
        self._end = end
        # nucleotide sequence
        if self._cds_strand == '-':
            cds_seq = [str(Seq(x).reverse_complement()) for x in self._cds_seq]
            self.NTseq = np.fromiter("".join(cds_seq), dtype='U1') # numpy array of coding sequence
            self._cds_positions = np.concatenate([np.flip(np.arange(s, e+1)) for s, e in zip(self._cds_start, self._cds_end)])
        else:
            self.NTseq = np.fromiter("".join(self._cds_seq), dtype='U1') # numpy array of coding sequence
            self._cds_positions = np.concatenate([np.arange(s, e+1) for s, e in zip(self._cds_start, self._cds_end)])
        self.cds_length = len(self.NTseq)
        self.AAseq = np.fromiter(codonalign.codonseq.CodonSeq("".join(self.NTseq)).translate(), dtype='U1')
        self.samples = None
        self.locs = None
        self.variants = {}
        self.statistics = statistics
        self.vcf = vcf
        self.annotation = annotation
        self.fasta = fasta
    
    def load_variation(self, vcf, samples=None, filter=None, protein_fasta=None, locs=False):
        """Fetch variants for a given transcript from a VCF file."""
        self.vcf = vcf
        chrval = self._chromosome.split('_')[-1] if '_' in self._chromosome else self._chromosome
        callset = allel.read_vcf(vcf, region=f"{chrval}:{self._start}-{self._end}")
        if callset is None or len(callset['variants/POS']) == 0:
            print(f"No variants found for transcript {self.id} in VCF {vcf}.")
            return
        if samples is not None:
            callset, sampleIDs, sampleLOCs = self._sort_vcf(callset, samples, locs=locs)
            self.samples = sampleIDs
            self.locs = sampleLOCs
        if filter is not None:
            # Load the filter VCF file and apply it to the callset
            filter_callset = allel.read_vcf(filter, region=f'{chrval}:{self._start}-{self._end}', samples=sampleIDs)
            keeppos = np.isin(callset['variants/POS'], filter_callset['variants/POS'])
            for key in callset.keys():
                if key.startswith('variants/') or key.startswith('calldata/'):
                    callset[key] = callset[key][keeppos]
        cds_seq_array = get_nucleotide_sequences(callset, self._cds_positions, self.NTseq, self._cds_strand)
        aa_seq_array = get_aa_sequences(cds_seq_array, self.NTseq, fasta_check=protein_fasta)
        sitesval = mean_ss_sites(cds_seq_array, self.AAseq)
        synarr, nonarr = identify_ss_ns_sites(cds_seq_array, self.NTseq, aa_seq_array, self.AAseq)
        self.samples = samples if samples is not None else callset['samples'].tolist()
        self.variants = {
            'callset': callset,
            'cds_seq_array': cds_seq_array,
            'aa_seq_array': aa_seq_array,
            'synarr': synarr,
            'nonarr': nonarr,
            'mean_nssites': sitesval[1],
            'mean_ssites': sitesval[0]
            }
        return self.variants

    def calculate_statistics(self):
        """Calculate statistics for a given transcript."""
        cds_seq_array = convert_to_binaryarray(self.variants['cds_seq_array'], 
                                                self.NTseq)
        aa_array = convert_to_binaryarray(self.variants['aa_seq_array'], 
                                               self.AAseq)
        ss_array = convert_to_binaryarray(self.variants['synarr'],
                                           np.repeat('', len(self.NTseq)))
        ns_array = convert_to_binaryarray(self.variants['nonarr'],
                                           np.repeat('', len(self.NTseq)))
        stats = {}
        if self.variants is None or len(self.variants) == 0:
            print(f"No variants loaded for transcript {self.id}. Cannot calculate statistics.")
            return stats
        if 'pi' in self.statistics:
            stats['pi_gene'] = calc_pi(allel.GenotypeArray(self.variants['callset']['calldata/GT']), 
                                       len(self.variants['callset']['variants/POS']))
            stats['pi_nt'] = calc_pi(cds_seq_array, self.cds_length)
            stats['pi_aa'] = calc_pi(aa_array, np.sum(self.variants['mean_nssites']))
            stats['pi_syn'] = calc_pi(ss_array, np.sum(self.variants['mean_ssites']))
            stats['pi_non'] = calc_pi(ns_array, np.sum(self.variants['mean_nssites']))
        if 'theta' in self.statistics:
            stats['theta_gene'] = calc_theta(allel.GenotypeArray(self.variants['callset']['calldata/GT']), 
                                       len(self.variants['callset']['variants/POS']))
            stats['theta_nt'] = calc_theta(cds_seq_array, self.cds_length)
            stats['theta_aa'] = calc_theta(aa_array, np.sum(self.variants['mean_nssites']))
            stats['theta_syn'] = calc_theta(ss_array, np.sum(self.variants['mean_ssites']))
            stats['theta_non'] = calc_theta(ns_array, np.sum(self.variants['mean_nssites']))
        if 'tajD' in self.statistics:
            stats['tajD_gene'] = calc_taj(allel.GenotypeArray(self.variants['callset']['calldata/GT']), 
                                       len(self.variants['callset']['variants/POS']))
            stats['tajD_nt'] = calc_taj(cds_seq_array, self.cds_length)
            stats['tajD_aa'] = calc_taj(aa_array, np.sum(self.variants['mean_nssites']))
            stats['tajD_syn'] = calc_taj(ss_array, np.sum(self.variants['mean_ssites']))
            stats['tajD_non'] = calc_taj(ns_array, np.sum(self.variants['mean_nssites']))
        if 'fst' in self.statistics and self.locs is not None:
            fstmat_nt = calc_fst_pairwise(self, cds_seq_array)
            fstmat_aa = calc_fst_pairwise(self, aa_array)
            fstmat_syn = calc_fst_pairwise(self, ss_array)
            fstmat_non = calc_fst_pairwise(self, ns_array)
            stats['fst_nt'] = triang2df_fst(fstmat_nt)
            stats['fst_aa'] = triang2df_fst(fstmat_aa)
            stats['fst_syn'] = triang2df_fst(fstmat_syn)
            stats['fst_non'] = triang2df_fst(fstmat_non)
        self.statistics = stats
        return stats

    def _sort_vcf(self, callset, samples, locs=False):
        """Sort VCF file by samples"""
        sampleIDs = samples['sample_id'].tolist()
        sampleIDs = np.array(sampleIDs)
        sample_indices = np.array([[-1, np.nan] if len(np.where(callset['samples'] == i)[0]) == 0 # remove if no match in VCF
                        else [[-1 if len(np.where(sampleIDs == i)[0]) == 0 # remove if no match in metadata
                        else np.where(sampleIDs == i)[0][0]][0], np.where(callset['samples'] == i)[0][0]]
                        for i in sampleIDs])
        sample_indices = sample_indices[sample_indices[:,0] != -1]
        sample_indices = sample_indices.astype(int)
        sampleIDs = sampleIDs[sample_indices[:,0]]
        callset['calldata/GT'] = allel.GenotypeArray(callset['calldata/GT'][:,sample_indices[:,1],:])
        callset['samples'] = sampleIDs
        # double check that genotypes match
    # assert (genotypes[:,0:1] == allel.GenotypeArray(callset['calldata/GT'][:,sample_indices[0:1,1],:])).all()
        # re-sort metadata
        samples.index = samples['sample_id']
        samples = samples.reindex(sampleIDs)
        if locs:
            assert ('latitude' in samples.columns and 'longitude' in samples.columns) or ('x' in samples.columns and 'y' in samples.columns), "Metadata file must contain 'latitude' and 'longitude' or 'x' and 'y' columns for location data."
            try: 
                sampleLOCs = np.array([samples['longitude'], samples['latitude']]).T
            except:
                sampleLOCs = np.array([samples['x'], samples['y']]).T
        else:
            sampleLOCs = None
        return(callset, sampleIDs, sampleLOCs)
        

